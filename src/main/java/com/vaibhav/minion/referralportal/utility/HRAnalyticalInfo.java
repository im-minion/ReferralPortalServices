package com.vaibhav.minion.referralportal.utility;

public class HRAnalyticalInfo {
    private long allReferralsCount, allJobsCount, allHiredCount;

    public long getAllReferralsCount() {
        return allReferralsCount;
    }

    public void setAllReferralsCount(long allReferralsCount) {
        this.allReferralsCount = allReferralsCount;
    }

    public long getAllJobsCount() {
        return allJobsCount;
    }

    public void setAllJobsCount(long allJobsCount) {
        this.allJobsCount = allJobsCount;
    }

    public long getAllHiredCount() {
        return allHiredCount;
    }

    public void setAllHiredCount(long allHiredCount) {
        this.allHiredCount = allHiredCount;
    }
}
